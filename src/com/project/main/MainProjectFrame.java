    public int getNextInvoiceNumber() {
        int ihNumber = 0;
        
        for (InvoiceHeader invoiceHeader : getInvoiceHeaders()) {
            if (invoiceHeader.getIhNumber() > ihNumber) 
                ihNumber = invoiceHeader.getIhNumber();
        }
        
        return ++ihNumber;
    }
