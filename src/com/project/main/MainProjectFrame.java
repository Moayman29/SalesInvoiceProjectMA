    public ArrayList<InvoiceHeader> getInvoiceHeaders() {
        if (invoiceHeaders == null) invoiceHeaders = new ArrayList<>();
        return invoiceHeaders;
    }

    public void setInvoiceHeaders(ArrayList<InvoiceHeader> invoiceHeaders) {
         this.invoiceHeaders = invoiceHeaders;
 }

    public InvoicesHeaderTable getInvoicesHeaderTable() {
        if (invoicesHeaderTable == null) {
            invoicesHeaderTable = new InvoicesHeaderTable(getInvoiceHeaders());
        }
        return invoicesHeaderTable;
    }

